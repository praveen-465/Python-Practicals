#range
x= range(6)#when range is used it displays 0 to n-1 
for i in x:
    print(i)
    
print(' ')

x=range(3,6)#will display numbers from 1st number given to before number of last num
for i in x:
    print(i)
    
print(' ')

x=range(10,-2,-1)#will display numbers from 1st num given with stepsize 2 till before num of last num
for i in x:
    print(i)