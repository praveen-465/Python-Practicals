#Retriving pieces of sting using slice operator
p='hello everyone i am praveen'
s=str(p)
print(s)  #will display whole string (s)
print(s[0])  #will dispaly 0th character from str
print(s[4:8])   #will display from 4th to 8th charaters
print(s[13:])   #will display from 11th charcters to end
print(s[-1])   #will display 1st character from end